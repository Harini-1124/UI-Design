class switchdemo{
void sdemo(String fruit){
	switch(fruit){
		case 'apple'||'Apple':
			print("apples are available");
		case 'mango'||'Mango':
			print("mangoes are available");
			break;
		case 'banana'||'Banana':
			print("bananas are available");
			break;
	default:
		print("the fruits are not available in shop");
	}
}
}

		